using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


namespace ConsoleApp2
{

    namespace ConsoleApp2
    {
        class Program
        {
            static void Main(string[] args)
            {
                int soma = 0;

                for (int i = 1; i <= 1000; i++)
                {
                    soma += i;
                }
                Console.WriteLine("A soma dos números de 1 a 1000 é: " + soma);
                Console.ReadLine();
            }
        }
    }
   
}
